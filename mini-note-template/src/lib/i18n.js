window.useTranslations = function () {
  const [lang, setLangState] = React.useState(() => window.Prefs.get('lang', 'en'));
  const setLang = React.useCallback((v) => { setLangState(v); window.Prefs.set('lang', v); }, []);
  const toggleLang = React.useCallback(() => {
    setLangState((cur) => {
      const next = cur === 'en' ? 'ru' : 'en';
      window.Prefs.set('lang', next);
      return next;
    });
  }, []);
  return { lang, setLang, toggleLang, t: window.STR[lang] || window.STR.en };
};
